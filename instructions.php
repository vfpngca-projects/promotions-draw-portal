  <h2>VF-PNG Promotions Draw Portal</h2>
  <h3 class="text-primary">Instructions of use</h3>

   <h4> Preparing of the Data for Upload (Important)</h4>
   <ol>
     <li>Use  excel or any spread sheet application  to enter  required data needed to run the draws;
     Make sure to  enter them on one  column, without any column titles (remove any column titles)**;</li>
  <li>Save the file in a (.csv) format and Upload through the LOAD ENTRIES button.</li>

  <li>Click the GO button to run the Draw</li>
  <li>Click the Save Winner button below the draw result to save the result of the draw. Repeat the  process for the number of draws you want to make.</li>
  <li>Click SHOW RESULTS button on the top to view the populated results of the draws being made.</li>
  <li>Click  Download results button from the popup dialogue to download the results in a .CSV file format.</li>
  <li>Click the reset button on the top left to reset all the Data being uploaded or Populated within the Draw.</li>
   </ol>

   <p>Note*:
   You will have to reupload the data file and restart the whole process after resetting.</p>

